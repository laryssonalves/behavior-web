export const brazilStatesChoices = (): Choice[] => {
  const choiceList: Choice[] = []
  Object.keys(BRAZIL_STATES).forEach((value) => {
    choiceList.push({ value: value, name: BRAZIL_STATES[value] })
  })

  return choiceList
}
